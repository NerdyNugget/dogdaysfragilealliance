function GetVehicleWindows(veh,hash)
 if hash==nil then hash=GetEntityModel(veh) end
 local window_indexes=VEHICLE_WINDOWS[hash]
 local ret=0
 if window_indexes~=nil then
  local msg1="Vehicle hash="..hash
  print(msg1)
  for k,v in pairs(window_indexes) do
   local msg2="Checking window "..k.."..."
   print(msg2)
   local success,err=pcall(IsVehicleWindowIntact,veh,k)
   if success then
    if not err then
     ret=(ret|v)
    end
   else
    report_client_error(msg1..'\n'..msg2..'\n'..err)
   end
  end
 end
 return ret
end
function SmashVehicleWindows(veh,windows,hash)
 if hash==nil then hash=GetEntityModel(veh) end
 local window_indexes=VEHICLE_WINDOWS[hash]
 if window_indexes~=nil then
  local msg1="Vehicle hash="..hash
  print(msg1)
  for k,v in pairs(window_indexes) do
   if (windows&v)==v then
    local msg2="Smashing window "..k.."..."
    print(msg2)
    local success,err=pcall(SmashVehicleWindow,veh,k)
    if not success then
     report_client_error(msg1..'\n'..msg2..'\n'..err)
    end
   end
  end
 end
